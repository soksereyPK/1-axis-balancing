int notes_poter = sizeof(melody_poter) / sizeof(melody_poter[0]) / 2;
int wholenote_poter = (60000 * 4) / tempo_poter;
int divider_poter = 0, noteDuration_poter = 0;

void harryPoter(){
  for (int thisNote = 0; thisNote < notes_poter * 2; thisNote = thisNote + 2) {

    // calculates the duration of each note
    divider_poter = melody_poter[thisNote + 1];
    if (divider_poter > 0) {
      // regular note, just proceed
      noteDuration_poter = (wholenote_poter) / divider_poter;
    } else if (divider_poter < 0) {
      // dotted notes are represented with negative durations!!
      noteDuration_poter = (wholenote_poter) / abs(divider_poter);
      noteDuration_poter *= 1.5; // increases the duration in half for dotted notes
    }

    // we only play the note for 90% of the duration, leaving 10% as a pause
    tone(buzzer, melody_poter[thisNote], noteDuration_poter*0.9);

    // Wait for the specief duration before playing the next note.
    delay(noteDuration_poter);
    
    // stop the waveform generation before the next note.
    noTone(buzzer);
  }
}

